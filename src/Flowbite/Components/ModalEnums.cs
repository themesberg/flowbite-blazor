namespace Flowbite.Components;

/// <summary>
/// Defines the size options for a modal dialog.
/// </summary>
public enum ModalSize
{
    /// <summary>
    /// Small modal size (max-w-sm).
    /// </summary>
    Small,
    
    /// <summary>
    /// Medium modal size (max-w-md).
    /// </summary>
    Medium,
    
    /// <summary>
    /// Large modal size (max-w-lg).
    /// </summary>
    Large,
    
    /// <summary>
    /// Extra large modal size (max-w-xl).
    /// </summary>
    ExtraLarge,
    
    /// <summary>
    /// 2XL modal size (max-w-2xl).
    /// </summary>
    TwoExtraLarge,
    
    /// <summary>
    /// 3XL modal size (max-w-3xl).
    /// </summary>
    ThreeExtraLarge,
    
    /// <summary>
    /// 4XL modal size (max-w-4xl).
    /// </summary>
    FourExtraLarge,
    
    /// <summary>
    /// 5XL modal size (max-w-5xl).
    /// </summary>
    FiveExtraLarge,
    
    /// <summary>
    /// 6XL modal size (max-w-6xl).
    /// </summary>
    SixExtraLarge,
    
    /// <summary>
    /// 7XL modal size (max-w-7xl).
    /// </summary>
    SevenExtraLarge,
    
    /// <summary>
    /// Default modal size (max-w-2xl).
    /// </summary>
    Default = TwoExtraLarge
}

/// <summary>
/// Defines the position options for a modal dialog.
/// </summary>
public enum ModalPosition
{
    /// <summary>
    /// Position the modal at the top left of the screen.
    /// </summary>
    TopLeft,
    
    /// <summary>
    /// Position the modal at the top center of the screen.
    /// </summary>
    TopCenter,
    
    /// <summary>
    /// Position the modal at the top right of the screen.
    /// </summary>
    TopRight,
    
    /// <summary>
    /// Position the modal at the center left of the screen.
    /// </summary>
    CenterLeft,
    
    /// <summary>
    /// Position the modal at the center of the screen (default).
    /// </summary>
    Center,
    
    /// <summary>
    /// Position the modal at the center right of the screen.
    /// </summary>
    CenterRight,
    
    /// <summary>
    /// Position the modal at the bottom left of the screen.
    /// </summary>
    BottomLeft,
    
    /// <summary>
    /// Position the modal at the bottom center of the screen.
    /// </summary>
    BottomCenter,
    
    /// <summary>
    /// Position the modal at the bottom right of the screen.
    /// </summary>
    BottomRight
}
